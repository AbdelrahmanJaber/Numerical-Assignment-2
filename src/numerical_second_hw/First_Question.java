    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
if(x.getText().equals(""))
    JOptionPane.showMessageDialog(First_Question.this,"A is empty");
else if(y.getText().equals(""))
    JOptionPane.showMessageDialog(First_Question.this,"B is empty");
else if(z.getText().equals(""))
    JOptionPane.showMessageDialog(First_Question.this,"# of iterations is empty");
else{
    String a=x.getText();
    String b=y.getText();
    String c=z.getText();
    
     for(int i=0;i<a.length();i++) {
      if(!(a.charAt(i)>='0'&&a.charAt(i)<='9')&&(a.charAt(i)!='.')){
      JOptionPane.showMessageDialog(First_Question.this, "A Should be a number ", "WARNING", JOptionPane.WARNING_MESSAGE);
      return;
      }
    }
    
     for(int i=0;i<b.length();i++) {
      if(!(b.charAt(i)>='0'&&b.charAt(i)<='9')&&(b.charAt(i)!='.')){
      JOptionPane.showMessageDialog(First_Question.this, "B Should be a number ", "WARNING", JOptionPane.WARNING_MESSAGE);
      return;
      }
    }
     for(int i=0;i<c.length();i++) {
      if(!(c.charAt(i)>='0'&&c.charAt(i)<='9')&&(c.charAt(i)!='.')){
      JOptionPane.showMessageDialog(First_Question.this, "# of iterations Should be a number ", "WARNING", JOptionPane.WARNING_MESSAGE);
      return;
      }
    }
     
       if(a.charAt(0)=='.'){
        JOptionPane.showMessageDialog(First_Question.this, "A can not START with . ", "WARNING", JOptionPane.WARNING_MESSAGE);
      return;
    }
    if(b.charAt(0)=='.'){
        JOptionPane.showMessageDialog(First_Question.this, "B can not START with . ", "WARNING", JOptionPane.WARNING_MESSAGE);
      return;
    }
    if(c.charAt(0)=='.'){
        JOptionPane.showMessageDialog(First_Question.this, "# of iterations can not START with . ", "WARNING", JOptionPane.WARNING_MESSAGE);
      return;
    }
     
     
     
    if(a.charAt(a.length()-1)=='.'){
        JOptionPane.showMessageDialog(First_Question.this, "A can not END with . ", "WARNING", JOptionPane.WARNING_MESSAGE);
      return;
    }
    if(b.charAt(b.length()-1)=='.'){
        JOptionPane.showMessageDialog(First_Question.this, "B can not END with . ", "WARNING", JOptionPane.WARNING_MESSAGE);
      return;
    }
    if(c.charAt(c.length()-1)=='.'){
        JOptionPane.showMessageDialog(First_Question.this, "# of iterations can not END with . ", "WARNING", JOptionPane.WARNING_MESSAGE);
      return;
    }
   double x_opt=0; 
   double A=Double.parseDouble(x.getText());
   double B=Double.parseDouble(y.getText());
   if(A>=B){
   JOptionPane.showMessageDialog(First_Question.this,"A Should be less than B");
   return; 
}
   double d=calc_d(A,B);
   double x1=A+d;
   double x2=B-d;
   if(x1>x2){
       double tmp=x1;
       x1=x2;
       x2=tmp;
   }
   double f_x1=y(x1);
   double f_x2=y(x2);
   Integer counter=Integer.parseInt(z.getText());
   
   while(true){
       if (counter<=0)
           break;
   
 
   if (f_x1>f_x2){
       B=x2;
       x_opt=x1;
       d=calc_d(A,B);
       x2=B-d;
       if(x1>x2){
       double tmp=x1;
       x1=x2;
       x2=tmp;
       counter--;
   }
   }
       else{
       A=x1;
       x_opt=x2;
       d=calc_d(A,B);
       x1=A+d;
       if(x1>x2){
       double tmp=x1;
       x1=x2;
       x2=tmp;  
       counter--;
       }
   
   }
    f_x1=y(x1);
    f_x2=y(x2);
   }
   l.setText(x_opt+"");
} 
    }//GEN-LAST:event_jButton3ActionPerformed

