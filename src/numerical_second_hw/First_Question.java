    public double convert (double x){
    double r ;
    r=(Math.PI*x)/180;
    return r;
 }
    
    
    
    
    public double y (double x){
    double w ;
    
    if(o.getText().equals("")){
    v_note=25;}
    else{
    v_note=Double.parseDouble(o.getText());
    }
    if(p.getText().equals("")){
    theta_note=convert(50);}
    else{
    theta_note=convert(Double.parseDouble(p.getText()));
    }
    w=Math.tan(theta_note)*x -(9.81*Math.pow(x,2))/(2*Math.pow(v_note,2)*Math.pow(Math.cos(theta_note),2))+1;
    return w;
    
}
   

    
    
    public double calc_d(double a,double b){
        double t;
        t=0.618*(b-a);
        return t;
         
    }
    
    
    
    
